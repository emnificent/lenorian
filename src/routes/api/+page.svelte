<h1>API Documentation</h1>
<h2>/convert</h2>
<p>convert Gregorian date to Eleanorian date</p>
<p>return JSON</p>
<h3>optional parameters</h3>
<ul>
  <li>gdate — convert specific Gregorian date (yyyy-mm-dd, yyyy/mm/dd or timestamps), otherwise uses today</li>
  <li>utc — work in UTC timezone</li>
</ul>
<h2>/calendar</h2>
<p>generate calendar for the Eleanorian year</p>
<p>return JSON</p>
<h3>optional parameter</h3>
<ul>
  <li>year — pick a specific Eleanorian year to generate the calendar, otherwise uses current Eleanorian year</li>
</ul>