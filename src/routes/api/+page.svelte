<h1>API Documentation</h1>
<h2>/convert</h2>
<p>convert Gregorian date to Eleanorian date</p>
<p>return JSON</p>
<h3>optional parameter</h3>
<p>gdate — convert specific Gregorian date (YYYY-MM-DD), otherwise uses today (UTC)</p>
<h2>/calendar</h2>
<p>generate calendar for the Eleanorian year</p>
<p>return JSON</p>
<h3>optional parameter</h3>
<p>year — pick a specific Eleanorian year to generate the calendar, otherwise uses current Eleanorian year</p>